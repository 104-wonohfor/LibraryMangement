    private void btnSendCodeActionPerformed(java.awt.event.ActionEvent evt) {
        String email = txtEmailAdress.getText();

        if (email.equals("")) {
            JOptionPane.showMessageDialog(this, "Please fill in Email Adress", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String cardId = new ReaderDAO().getCardIdByEmail(email);

        if (cardId.equals("NULL")) {
            JOptionPane.showMessageDialog(this, "Email not found", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            JOptionPane.showMessageDialog(this, "Code has been sent to your email", "Success", JOptionPane.INFORMATION_MESSAGE);
            code = "123456";

            txtEmailAdress.setEditable(false);

            lblConfirmationCode.setVisible(true); lblConfirmationCode.setEnabled(true);
            txtConfirmationCode.setVisible(true); txtConfirmationCode.setEnabled(true);
            btnVerifyCode.setVisible(true); btnVerifyCode.setEnabled(true);

            return;
        }
    }

