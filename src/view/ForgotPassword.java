    private String hashPassword(String password) {
        byte[] temp = password.getBytes();
        byte[] hashData = null;

        try {
            MessageDigest md = MessageDigest.getInstance("MD5");
            hashData = md.digest(temp);
        } catch (NoSuchAlgorithmException e) {
            e.printStackTrace();
        }

        StringBuilder hashPass = new StringBuilder();

        for (byte item : hashData) {
            hashPass.append(Byte.toUnsignedInt(item));
        }

        return hashPass.toString();
    }

    private void btnEditPasswordActionPerformed(java.awt.event.ActionEvent evt) {
        String newPassword = new String(txtNewPassword.getPassword());
        String retypeNewPassword = new String(txtRetypeNewPassword.getPassword());

        if (newPassword.equals("") || retypeNewPassword.equals("")) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (!newPassword.equals(retypeNewPassword)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (new String(txtNewPassword.getPassword()).matches("^.*\\s.*$")) {
            JOptionPane.showMessageDialog(null, "New password must not have space", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (!new String(txtNewPassword.getPassword()).matches("^.*[A-Z].*$")) {
            JOptionPane.showMessageDialog(null, "New password must have at least 1 uppercase character", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (!new String(txtNewPassword.getPassword()).matches("^.*[a-z].*$")) {
            JOptionPane.showMessageDialog(null, "New password must have at least 1 lowercase character", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (!new String(txtNewPassword.getPassword()).matches("^.*\\d.*$")) {
            JOptionPane.showMessageDialog(null, "New password must have at least 1 digit", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (!new String(txtNewPassword.getPassword()).matches("^.*[!@#$%^&*()_+\\-=\\[\\]{};':\"\\\\|,.<>\\/?].*$")) {
            JOptionPane.showMessageDialog(null, "New password must have at least 1 special character", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (new String(txtNewPassword.getPassword()).length() < 8 || new String(txtNewPassword.getPassword()).length() > 50) {
            JOptionPane.showMessageDialog(null, "New password must have at least 8 characters and at most 50 characters", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } 

        String email = txtEmailAdress.getText();
        String idNumber = new ReaderDAO().getIdNumberByEmail(email);

        if (idNumber.equals("NULL")) {
            JOptionPane.showMessageDialog(this, "Email not found", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            String newPasswordHash = hashPassword(newPassword);
            
            if (new ReaderDAO().editPasswordByIDNumber(idNumber, newPasswordHash)) {
                JOptionPane.showMessageDialog(this, "Password has been changed", "Success", JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
                login.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to change password", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }        
    }

